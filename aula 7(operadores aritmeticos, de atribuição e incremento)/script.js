//quando usar o "+" com number, vou ter o resultado de adição//
const num1 = 5;
const num2 = 10;
console.log(num1 + num2);
//resultado = 15//

//agora se colocar uma string, vou ter o resultado de concatenação//
const num3 = '5';
const num4 = 10;
